package day02_intro_to_variables;

public class VariableDeclarations {
    public static void main(String [] args){
        // declare single variable
        int apples;

        //declare multiple variables in a single statement
        int bananas,oranges,mangoes,lemons;

        //declare single variable and assign value 1 statement
        int kiwi=2;

        //declare and assign value to multiple variables
        //in single statement
        int watermelon = 4, grapes =45, cherries = 33, apricots = 0;
    }
}
