package day02_intro_to_variables;

public class VariablesIntro {
    public static void main(String [] args){
        //declare variable
        int count;
        //assign value 10 to count
        count=10;
        System.out.println(count);

        //declare variable apples
        int apples;
        //assign 5 to apples
        apples=5;
        System.out.println(apples);


        //Combine variable declaration and assign them together
        int students=100; int teachers=85; int people=10000;

        System.out.println(students);
        System.out.println(teachers);
        System.out.println(people);

        people=20000;
        System.out.println(people);
    }

}
